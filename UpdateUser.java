    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String username = txtuserName.getText();
        String name = txtname.getText();
        String email = txtemail.getText();
        String role = comboUserRole.getSelectedItem().toString();
        String mobileNumber = txtmobileNumber.getText();
        SimpleDateFormat outputFormat = new SimpleDateFormat("yyyy-MM-dd");
        Date date = dateDOB.getDate();
        String dob = "";

        if (date != null) {
            dob = outputFormat.format(dateDOB.getDate());
        }
        if (name.equals("")) {
            JOptionPane.showMessageDialog(null, "Name Feild is Required");
        } else if (dob.equals("")) {
            JOptionPane.showMessageDialog(null, "DOB Feild is Required");
        } else if (mobileNumber.equals("")) {
            JOptionPane.showMessageDialog(null, "Mobile Number Feild is Required");
        } else if (!mobileNumber.matches(mobilePattern) || mobileNumber.length() != 10) {
            JOptionPane.showMessageDialog(null, "Mobile Number Feild is Invalid");
            txtmobileNumber.setText("");
            txtmobileNumber.requestFocus();

        } else if (email.equals("")) {
            JOptionPane.showMessageDialog(null, "Email Feild is Required");
        } else if (!email.matches(emailPattern)) {
            JOptionPane.showMessageDialog(null, "Email Feild is Invalid");
            txtemail.setText("");
            txtemail.requestFocus();
        } else if (role.equals("None")) {
            JOptionPane.showMessageDialog(null, "Please Select the User Role");
        } else if (username.equals("")) {
            JOptionPane.showMessageDialog(null, "Username Feild is Required");
            txtuserName.requestFocus();
        } else {
            PreparedStatement ps = null;
            try {
                String url = "jdbc:mysql://localhost:3306/pharmacy";
                con = DriverManager.getConnection(url, "root", "lahiru@123");
                ps = con.prepareStatement("UPDATE useprofile SET name=?,dob=?,email=?,role=?,mobilenumber=? WHERE username=?");
                ps.setString(1, name);
                ps.setString(2, dob);
                ps.setString(3, email);              
                ps.setString(4, role);
                ps.setString(5, mobileNumber);
                ps.setString(6, username);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "User Updated successful!");
                setVisible(false);
                new UpdateUser().setVisible(true);
                

            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Update User failed. Please try again.");

            }
        }


    }//GEN-LAST:event_jButton3ActionPerformed

