    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String userName = txtuserName.getText();

        if (userName.equals("")) {
            JOptionPane.showMessageDialog(null, "Username Feild is Required");
        } else {
            SimpleDateFormat outputFormat = new SimpleDateFormat("yyyy-MM-dd");
            try {
                String url = "jdbc:mysql://localhost:3306/pharmacy";
                con = DriverManager.getConnection(url, "root", "lahiru@123");
                Statement stmt = con.createStatement();
                ResultSet rs = stmt.executeQuery("SELECT * FROM useprofile WHERE username='" + userName + "'");
                while (rs.next()) {
                    txtuserName.setEditable(true);
                    checkUserExist = 1;
                    txtname.setText(rs.getString("name"));
                    txtemail.setText(rs.getString("email"));
                    txtmobileNumber.setText(rs.getString("mobilenumber"));
                    String dob = rs.getString("dob");
                    if (dob != "") {
                        try {
                            dateDOB.setDate(outputFormat.parse(dob));

                        } catch (Exception e) {

                        }

                    }
                    if (rs.getString("role").equals("Admin")) {
                        comboUserRole.removeAllItems();
                        comboUserRole.addItem("Admin");
                        comboUserRole.addItem("Pharmacist");
                        comboUserRole.addItem("Suported Staff");
                        comboUserRole.addItem("None");

                    } else if (rs.getString("role").equals("Pharmacist")) {
                        comboUserRole.removeAllItems();
                        comboUserRole.addItem("Pharmacist");
                        comboUserRole.addItem("Suported Staff");
                        comboUserRole.addItem("Admin");
                        comboUserRole.addItem("None");

                    } else {
                        comboUserRole.removeAllItems();
                        comboUserRole.addItem("Suported Staff");
                        comboUserRole.addItem("Admin");
                        comboUserRole.addItem("Pharmacist");
                        comboUserRole.addItem("None");
                    }

                }
            } catch (SQLException e) {
                e.printStackTrace();
            }
            if (checkUserExist == 0) {
                JOptionPane.showMessageDialog(null, "Username name is does not Exist");
                txtuserName.setText("");
                txtuserName.requestFocus();
                txtname.setText("");
                txtemail.setText("");
                txtmobileNumber.setText("");
                comboUserRole.removeAllItems();
                comboUserRole.addItem("None");
                dateDOB.setDateFormatString("");

            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

